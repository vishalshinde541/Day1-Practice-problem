package com.bridgelabz;



public class DefaultValueOfPrimitiveDataType {
	
	static byte num1;
	static short num2;
	static int num3;
	static long num4;
	static double num5;
	static char char1;
	static float float1;
	static boolean boolean1;
	
	public static void main(String[] args) {
		
		System.out.println("Default value of primitive Data Type ");
		
		System.out.println("byte : " + num1);
		System.out.println("short : " + num2);
		System.out.println("int :" + num3);
		System.out.println("long :" + num4);
		System.out.println("double :" + num5);
		System.out.println("char : " + char1);
		System.out.println("float : " + float1);
		System.out.println("boolean :" + boolean1);
	}
	
	

}
