package com.bridgelabz;

public class CheckTwoStringsAreEqualOrNot {
	
	public static void main(String[] args) {
		String s1 = "abc";
		String s2 = "xyz";
		
		if (s1.equals(s2)) {
			System.out.println("Two strings are equal");
		}
		else {
			System.out.println("Two strings are Not equal");
		}
		
	}

}
